package flight.management.system;

public class Customer {
    private String name;
    private Flight f;

    public Customer(String name){
        this.name=name;
        this.f=this.f;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public Flight getFlight() {
        return this.f;
    }

    public void setFlight(Flight f) {
        this.f = f;
    }

}