package flight.management.system;

public class Airport {
    private String name;
    private String id;

    public Airport(){
        this.name=this.name;
        this.id=this.id;
    }

    public String getName(){
        return this.name;
    }

    public void setName(String name){
        this.name=name;
    }

    public String getId(){
        return this.id;
    }

    public void setId(String id){
        this.id=id;
    }
}